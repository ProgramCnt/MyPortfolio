package com.psj.project.model;

public class EducationModel {
	String schoolName;
	String schoolType;
	String admitGraduate;
	String entranceCeremony;
	String major;
	String learnedSubject;
	String description;
	
	/**
	 * @return the schoolName
	 */
	public String getSchoolName() {
		return schoolName;
	}

	/**
	 * @param schoolName the schoolName to set
	 */
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}

	/**
	 * @return the schoolType
	 */
	public String getSchoolType() {
		return schoolType;
	}
	
	/**
	 * @param schoolType the schoolType to set
	 */
	public void setSchoolType(String schoolType) {
		this.schoolType = schoolType;
	}
	
	/**
	 * @return the admitGraduate
	 */
	public String getAdmitGraduate() {
		return admitGraduate;
	}
	
	/**
	 * @param admitGraduate the admitGraduate to set
	 */
	public void setAdmitGraduate(String admitGraduate) {
		this.admitGraduate = admitGraduate;
	}
	
	/**
	 * @return the entranceCeremony
	 */
	public String getEntranceCeremony() {
		return entranceCeremony;
	}
	
	/**
	 * @param entranceCeremony the entranceCeremony to set
	 */
	public void setEntranceCeremony(String entranceCeremony) {
		this.entranceCeremony = entranceCeremony;
	}
	
	/**
	 * @return the major
	 */
	public String getMajor() {
		return major;
	}
	
	/**
	 * @param major the major to set
	 */
	public void setMajor(String major) {
		this.major = major;
	}
	
	/**
	 * @return the learnedSubject
	 */
	public String getLearnedSubject() {
		return learnedSubject;
	}
	
	/**
	 * @param learnedSubject the learnedSubject to set
	 */
	public void setLearnedSubject(String learnedSubject) {
		this.learnedSubject = learnedSubject;
	}
	
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
}
