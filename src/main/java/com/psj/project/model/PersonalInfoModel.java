package com.psj.project.model;

public class PersonalInfoModel {
	String birthday; 
	String address;
	String phoneNumber; 
	String imageOriName; 
	String imagePath;
	String imageUploadName;
	
	/**
	 * @return the birthday
	 */
	public String getBirthday() {
		return birthday;
	}
	
	/**
	 * @param birthday the birthday to set
	 */
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	
	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}
	
	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}
	
	/**
	 * @return the phoneNumber
	 */
	public String getPhoneNumber() {
		return phoneNumber;
	}
	
	/**
	 * @param phoneNumber the phoneNumber to set
	 */
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	
	/**
	 * @return the imageOriName
	 */
	public String getImageOriName() {
		return imageOriName;
	}
	
	/**
	 * @param imageOriName the imageOriName to set
	 */
	public void setImageOriName(String imageOriName) {
		this.imageOriName = imageOriName;
	}
	
	/**
	 * @return the imagePath
	 */
	public String getImagePath() {
		return imagePath;
	}
	
	/**
	 * @param imagePath the imagePath to set
	 */
	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}
	
	/**
	 * @return the imageUploadName
	 */
	public String getImageUploadName() {
		return imageUploadName;
	}
	
	/**
	 * @param imageUploadName the imageUploadName to set
	 */
	public void setImageUploadName(String imageUploadName) {
		this.imageUploadName = imageUploadName;
	}
}
